    public function reply(Request $request){
        $contact = Contact::find($request->id);
        $contact->update([
           'reply_message' => $request->reply_message,
            'status' => Contact::ISREPLY,
        ]);
        Mail::to(str_replace("\xE2\x80\x8B", "", $contact->email))->send(new ReplyContact($contact));
        return redirect()->route('contacts.index')->with('success', 'Email sent succesfully!');
    }
